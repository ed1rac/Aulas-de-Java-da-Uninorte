public class ContadorFor{	
	//método principal, onde inicia a execução do aplicativo java
	public static void main(String[] args){
		for(int i=0;i<=10;i++){
			System.out.print(i + " ");
		}
		System.out.println("\nAcabou!");
					
	}// fim do método main
}// fim da classe ContadorFor
